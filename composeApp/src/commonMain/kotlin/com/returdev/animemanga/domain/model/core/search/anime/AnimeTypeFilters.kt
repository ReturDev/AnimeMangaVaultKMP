package com.returdev.animemanga.domain.model.core.search.anime

/**
 * Enum class representing the available type filters for anime search results.
 *
 * The filters include:
 * - TV: Television series.
 * - MOVIE: Anime movies.
 * - OVA: Original Video Animation.
 * - SPECIAL: Special episodes or releases.
 * - ONA: Original Net Animation.
 * - MUSIC: Music-related anime.
 * - CM: Commercials.
 * - PV: Promotional videos.
 * - TV_SPECIAL: TV specials.
 */
enum class AnimeTypeFilters {

    TV,
    MOVIE,
    OVA,
    SPECIAL,
    ONA,
    MUSIC,
    CM,
    PV,
    TV_SPECIAL;

}