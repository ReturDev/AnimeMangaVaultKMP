package com.returdev.animemanga.domain.model.core.search.anime

/**
 * Enum class representing the available status filters for anime search results.
 *
 * The filters include:
 * - AIRING: Anime that are currently airing.
 * - COMPLETE: Anime that have finished airing.
 * - UPCOMING: Anime that are scheduled to air in the future.
 */
enum class AnimeStatusFilters {

    AIRING,
    COMPLETE,
    UPCOMING;

}
